fruits=['apple','mango','banana','grapes','chiku','papaya','orange']
print(fruits) #all fruits
print(fruits[0]) #only 1st fruit
print(fruits[0:3]) #from zero till 3 (excluded)--> 0 1 & 2
print(fruits[0:6]) #0 to 5
print(fruits[0:7])#0 to 6

print(fruits[0:3:1]) #from zero till 3 (excluded)--> 0 1 & 2
print(fruits[0:6:1]) #0 to 5
print(fruits[0:7:1])#0 to 6

print(fruits[0:7:2])#start from 0 increase it by 2 till 7--> 0 2 4 6

print(fruits[-1:-8:-1])